<?php

namespace App\Http\Controllers;

    use Illuminate\Http\Request;
    use App\Category;
    use App\movies;

class CategoryController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $categories = Category::orderBy('id','DESC')->paginate(5);
        return view('category.index',compact('categories'))
            ->with('i', ($request->input('page', 1) - 1) * 5);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('category.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'title' => 'required',
            'description' => 'required',
            'adult' => 'required',
        ]);


        Category::create($request->all());

        return redirect()->route('category.index')
                        ->with('Perfecte','Categoria Creada Correctement');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
         $arrayPeliculas = movies::where('category_id', $id)->get();
        return view('category.show',compact('arrayPeliculas'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $categories = Category::find($id);
        return view('category.edit',compact('categories'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'title' => 'required',
            'description' => 'required',
            'adult'=>'required',
        ]);


        Category::find($id)->update($request->all());

        return redirect()->route('category.index')
                        ->with('Perfecte','Categoria modificada correctament');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
         Category::find($id)->delete();
        return redirect()->route('category.index')
                        ->with('Perfecte','Categoria eliminada correctamet');
    }
}
